# Franquias conhecidas — desqualificam automaticamente
FRANCHISE_BLACKLIST = [
    "mcdonald's", "burger king", "subway", "starbucks", "habib's", "bob's",
    "giraffas", "spoleto", "china in box", "cacau show", "o boticário",
    "natura", "riachuelo", "renner", "magazine luiza", "casas bahia",
    "americanas", "smart fit", "bluefit",
]


def get_niche(nicho: str) -> dict | None:
    """Busca nicho por key ou nome parcial."""
    nicho_lower = nicho.lower().strip()

    # Match exato por key
    if nicho_lower in NICHES:
        return NICHES[nicho_lower]

    # Match parcial por nome ou keywords
    for key, data in NICHES.items():
        if nicho_lower in data["name"].lower():
            return data
        for kw in data["keywords"]:
            if nicho_lower == kw.lower():
                return data

    return None


def get_keywords(nicho: str) -> list[str]:
    """Retorna keywords para um nicho. Se nao encontrar, retorna [nicho]."""
    data = get_niche(nicho)
    if data:
        return data["keywords"]
    return [nicho]


def is_franchise(name: str) -> bool:
    """Verifica se o nome e de uma franquia conhecida."""
    name_lower = name.lower().strip()
    return any(f in name_lower for f in FRANCHISE_BLACKLIST)
